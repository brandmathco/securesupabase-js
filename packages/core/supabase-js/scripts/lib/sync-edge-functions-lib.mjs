import { cp, mkdir, readFile, stat, writeFile } from 'node:fs/promises';
import path from 'node:path';

const REQUIRED_DENO_IMPORTS = {
  '@supabase/auth-js': 'npm:@supabase/auth-js@2.75.0',
  '@supabase/functions-js': 'npm:@supabase/functions-js@2.4.4',
  '@supabase/postgrest-js': 'npm:@supabase/postgrest-js@1.19.4',
  '@supabase/realtime-js': 'npm:@supabase/realtime-js@2.11.10',
  '@supabase/storage-js': 'npm:@supabase/storage-js@2.8.0',
};

async function existsAsFile(filePath) {
  try {
    const s = await stat(filePath);
    return s.isFile();
  } catch {
    return false;
  }
}

async function readJsonIfExists(filePath) {
  if (!(await existsAsFile(filePath))) return null;
  const raw = await readFile(filePath, 'utf8');
  return JSON.parse(raw);
}

async function existsAsDirectory(dirPath) {
  try {
    const s = await stat(dirPath);
    return s.isDirectory();
  } catch {
    return false;
  }
}

async function minifyModuleCode(sourceCode) {
  let esbuild;
  try {
    ({ transform: esbuild } = await import('esbuild'));
  } catch {
    return {
      code: sourceCode,
      didMinify: false,
      reason: 'esbuild is not available in this environment',
    };
  }

  try {
    const result = await esbuild(sourceCode, {
      loader: 'js',
      format: 'esm',
      minify: true,
      legalComments: 'none',
      target: 'es2020',
    });
    return {
      code: result.code,
      didMinify: true,
      reason: null,
    };
  } catch (error) {
    return {
      code: sourceCode,
      didMinify: false,
      reason: error instanceof Error ? error.message : String(error),
    };
  }
}

async function scaffoldSecureEdgeTemplates({ templatesDir, functionsDir }) {
  if (!templatesDir) {
    return { didScaffold: false, reason: 'templates dir not provided' };
  }
  const resolvedTemplatesDir = path.resolve(templatesDir);
  if (!(await existsAsDirectory(resolvedTemplatesDir))) {
    return { didScaffold: false, reason: `templates dir not found: ${resolvedTemplatesDir}` };
  }

  const supabaseDir = path.resolve(functionsDir, '..');
  const mappings = [
    {
      src: path.join(resolvedTemplatesDir, 'functions', '_shared'),
      dst: path.join(functionsDir, '_shared'),
    },
    {
      src: path.join(resolvedTemplatesDir, 'functions', 'db-proxy'),
      dst: path.join(functionsDir, 'db-proxy'),
    },
    {
      src: path.join(resolvedTemplatesDir, 'functions', 'auth-proxy'),
      dst: path.join(functionsDir, 'auth-proxy'),
    },
    {
      src: path.join(resolvedTemplatesDir, 'functions', 'e2ee-public-key'),
      dst: path.join(functionsDir, 'e2ee-public-key'),
    },
    {
      src: path.join(resolvedTemplatesDir, 'migrations'),
      dst: path.join(supabaseDir, 'migrations'),
    },
    {
      src: path.join(resolvedTemplatesDir, 'secure-edge-templates'),
      dst: path.join(supabaseDir, 'secure-edge-templates'),
    },
  ];

  for (const mapping of mappings) {
    if (!(await existsAsDirectory(mapping.src))) continue;
    await mkdir(mapping.dst, { recursive: true });
    await cp(mapping.src, mapping.dst, { recursive: true, force: true });
  }

  return {
    didScaffold: true,
    reason: null,
    templatesDir: resolvedTemplatesDir,
  };
}

export async function syncEdgeFunctions({ functionsDir, sourceFile, minify = true, templatesDir = '' }) {
  const resolvedFunctionsDir = path.resolve(functionsDir);
  const resolvedSourceFile = path.resolve(sourceFile);
  const sharedDir = path.join(resolvedFunctionsDir, '_shared');
  const vendorDir = path.join(sharedDir, 'vendor', 'securesupabase');
  const vendorFile = path.join(vendorDir, 'index.mjs');
  const bridgeFile = path.join(sharedDir, 'securesupabase.ts');
  const denoJsonFile = path.join(resolvedFunctionsDir, 'deno.json');

  const scaffolding = await scaffoldSecureEdgeTemplates({
    templatesDir,
    functionsDir: resolvedFunctionsDir,
  });

  const sourceCode = await readFile(resolvedSourceFile, 'utf8');
  const { code: vendorCode, didMinify, reason } = minify
    ? await minifyModuleCode(sourceCode)
    : { code: sourceCode, didMinify: false, reason: 'minify disabled by flag' };

  await mkdir(vendorDir, { recursive: true });
  await writeFile(vendorFile, vendorCode, 'utf8');

  const bridgeSource =
    "// Auto-generated by securesupabase init/sync\n" +
    "export { createClient, createSecureClient } from './vendor/securesupabase/index.mjs';\n";
  await writeFile(bridgeFile, bridgeSource, 'utf8');

  let denoJson = await readJsonIfExists(denoJsonFile);
  if (!denoJson) denoJson = {};
  if (typeof denoJson !== 'object' || Array.isArray(denoJson)) {
    throw new Error(`Invalid deno.json format at ${denoJsonFile}`);
  }
  const existingImports =
    denoJson.imports && typeof denoJson.imports === 'object' && !Array.isArray(denoJson.imports)
      ? denoJson.imports
      : {};

  denoJson.lock = false;
  denoJson.imports = {
    ...REQUIRED_DENO_IMPORTS,
    ...existingImports,
  };
  await writeFile(denoJsonFile, `${JSON.stringify(denoJson, null, 2)}\n`, 'utf8');

  return {
    source: resolvedSourceFile,
    vendor: vendorFile,
    bridge: bridgeFile,
    denoJson: denoJsonFile,
    minified: didMinify,
    minifyReason: reason,
    scaffolded: scaffolding.didScaffold,
    scaffoldReason: scaffolding.reason,
    templatesDir: scaffolding.templatesDir ?? null,
  };
}

